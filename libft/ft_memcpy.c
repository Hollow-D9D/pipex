#include "libft.h"

void			*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned			int				i;
	unsigned const		char			*s;
	unsigned			char			*d;

	s = src;
	d = dest;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
